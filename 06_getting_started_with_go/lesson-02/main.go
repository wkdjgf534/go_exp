package main

import "fmt"

func main() {
	var myString string
	// String zero value is ""
	fmt.Println(myString)

	myString = "Welocme to code and learn"
	fmt.Println(myString)

	var myInt int
	// Zero value for number types is 0
	fmt.Println(myInt)

	myInt = 10
	fmt.Println(myInt)

	var myBool bool
	// Zero value for bool is false
	fmt.Println(myBool)

	myBool = true
	fmt.Println(myBool)
}
